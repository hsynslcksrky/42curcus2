int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 'A' && str[c] <= 'Z'))
		{
			c++;
		}
		else if (str[c] >= 'a' && str[c] <= 'z')
		{
			c++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
